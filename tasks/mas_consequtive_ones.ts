function findMaxConsecutiveOnes(nums: number[]): number {
    let maxOnes = 0;
    let currentOnes = 0;
    let currentZeroes = 0;
    
    for (let i = 0; i < nums.length; i++) {
        if (nums[i] === 1) {
            currentZeroes++;
            currentOnes++;
        } else {
            currentZeroes = currentOnes + 1;
            currentOnes = 0;
        }
        
        maxOnes = Math.max(maxOnes, currentOnes, currentZeroes);
    }
    
    return maxOnes;
};

